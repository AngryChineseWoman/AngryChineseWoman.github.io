		<h2>This Month's Author: Carl Hiaasen</h2>

		<img class="florida" src="images/florida.jpg" alt="florida" width="541" height="475">

<p>Carl Hiaasen has written a number of books for young readers that encompass the same degree of humor,
excitement, and humanity that he shares in his books for adults. The plots may make reality TV look
tame by comparison, but he almost always produces an entertaining read. 
</p>

<p> From his own <a href="http://www.carlhiaasen.com/" target="_blank">website</a> 
 we know he started his writing career as a reporter for the Miami Herald where he worked for many years.
This experience influences his writing style, the events seem to be shared with a sense of duty to observe
and share them with the reader. The fact that the events are full of absurdity, chaos, and just plain
craziness, are noted and documented. No judgy pants here. (But somehow, you know that he knows that you know how
off the wall the whole thing is.)
</p>

<p>
It takes reading just one of the books (a short list of some titels are below) to also make a reader wonder
whether or not Mr. Hiaasen was the original creator of the "Florida Man" meme that has taken on a life of its own. 
You can come to your own conclusion.... but even if you don't think he's the OG Florida Man, I bet he manages to make
you laugh.
</p>

