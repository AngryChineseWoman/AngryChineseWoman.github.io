<h2 id="robhiaasen">Hug your loved ones</h2>


<p>
We often turn to reading as a way to immerse ourselves in a story, time, or character and enjoy being
in a different world for a little while. The magic of turning words on a page into a place in your mind
is inexplicable and amazing. But sometimes, the harsh reality of reality clashes intersects our personal
world of pleasure and enjoyment. This happened to me when I heard about the tragedy of Rob Hiaasen, Carl's brother.
The <a href="https://www.miamiherald.com/opinion/editorials/article214042204.html" target="_blank">story</a>
was shocking, just as every other mass shooting is. But I was particularly moved by this event as I had
only recently discovered Carl Hiaasen's work and was reading one of his novels at the time.
</p>
<p>
If you do visit Carl's website, and take the time to scroll down a bit to read more, you'll come across 
some references to the loss of his brother Bob and a link to an interview he gave about it on NPR. I haven't
been able to bring myself to listen to it, but I will share the last line of his note about the news "Hug your loved ones 
like there's no tomorrow". The guy knows what he's talking about.
</p>

