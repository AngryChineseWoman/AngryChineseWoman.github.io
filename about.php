<p>I live in Santa Clara Valley in California, having moved here after growing up and finishing college in NYC.
When I started making a trip the library with my daughter a regular outing, I came to see what a vital part of 
the community a library can be. It had been a couple of decades since my previous trip to a local branch library, 
and it was clear to  me that the library of my childhood was not the same as the libraries I see today. 

A public library can be a source of information, services, community support, and accessibility. I was so inspired
by "rediscovering" the beauty of a library that I decided to pursue a Master of Library and Information Science. 
When I'm not working or "Mom-ing" I am chipping away at the MLIS program at San Jose State University.
</p>

<p> In fact, this website is a culmination of a semester's worth of work and my final project for the course. It has been
a combination of exciting with small victories "WOW - I made it green!" and frustrating "WHY is it still green??"
But there's no doubt that on the whole, I am so happy I took a chance and signed up for the class. This website is humble,
but it's mine and I'm really proud of it.
</p>


