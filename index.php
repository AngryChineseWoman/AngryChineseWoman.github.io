		<h2 id="yadefine">What is a Young Adult?</h2>
<p>
"Young adult" is an interesting term and can be a little hard to pin down. Merriam-Webster's <a href="https://www.merriam-webster.com/dictionary/young%20adult" target="_blank">
defintion</a> specifically references the term relative to how it is used in literature. It goes on to reference "adolescent"
and leaves it at that. The term from a more "biological" perspective can yield a fairly wide range of years. 
The World Health Organization, gives a range of <a href="https://www.who.int/southeastasia/health-topics/adolescent-health
" target="_blank">10 to 25 years of age. </a>
</p>

<p>
Because the definition of a "young adult" can span just a large number of years, jumping in to any book in the YA section
of the bookstore does not guarantee that you'll love it. If you are at one end of the of spectrum, the subject matter
that you find interesting could be very different than what someone at the other end is currently in to. Regardless of
which end of the range you are in today, keep looking for that next, new, favorite book. I'm sure it's out there. 
</p>


