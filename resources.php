		<h2>Resources for Young Adult readers</h2>


<p>There are a lot of different ways to find recommendations on Young Adult literature. Having a few different options is helpful
for gathering a variety of suggestions. Maybe you'll discover a new author, series, or an entire genre and become a fan.
And while most of us understand the enjoyment that comes from reading a favorite author for days on end, "tripping upon" a new character
or book can be incredibly exciting and joyous. I will be the first to admit, however, hunting and pecking through shelves of books 
(literally or figuratively) can be a daunting proposition. Looking for the proverbial "diamond in the rough" requires an investment
of time to do some research and/or a stroke of luck. 
For a little guidance to point you in a direction, here are some suggested resources for you to check out:

</p>


