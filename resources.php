		<h2>For Parents of readers</h2>

<p>There are lots of ways a reader can find something that looks interesting to read. In addition to the resources
provided for the readers, a parent may have additional interest about a particular book and the themes that may be covered.
Or if you aren't a parent, but want to gift an avid reader in your life with something new, doing a little research can be helpful.
</p>

<p>
 <a href="https://www.commonsensemedia.org/" target ="_blank">Common Sense Media</a> is
the gold standard for helping parents/adults read up on what tv shows, movies, and books may be about before letting their kids
jump in with both feet. The site allows you to search based on age, genre, and topics.
</p>
<p>
Another site that will may feel familiar to parents is part of the Scholastic organization. And it's specifically their 
<a href="https://bookwizard.scholastic.com/" target="_blank">Book Wizard</a> tool. There is a path to follow if you want to Discover
New Books, and you can enter in your reader's grade level, interests, and voila!
</p>

